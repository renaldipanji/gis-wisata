<p class="text-success">Sistem Informasi Geografis (bahasa Inggris: Geographic Information System disingkat GIS) adalah sistem 
informasi khusus yang mengelola data yang memiliki informasi spasial (bereferensi keruangan). 
Atau dalam arti yang lebih sempit, adalah sistem komputer yang memiliki kemampuan untuk membangun, 
menyimpan, mengelola dan menampilkan informasi berefrensi geografis, misalnya data yang diidentifikasi 
menurut lokasinya, dalam sebuah database. Para praktisi juga memasukkan orang yang membangun dan mengoperasikannya 
dan data sebagai bagian dari sistem ini.</p>

<p class="text-success"><strong>Sistem Informasi Geografis Parawisata Kota Bangun Kabupaten Kutai Kartanegara</strong> merupakan sistem
yang mengelolah tentang informasi geografis parawisata yang tedapat di Kota Bangun Kabupaten Kutai Kartanegara. Sistem ini menjelaskan
tentang penyebaran letak letak parawisata pada Kota Bangun Kabupaten Kutai Kartanegara, di sertakan dengan galery tentang
beberapa parawisata tersebut.</p>
